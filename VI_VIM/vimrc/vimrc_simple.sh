# example of a basic .vimrc file with some common settings:
" Enable line numbers
set number

" Use spaces instead of tabs
set expandtab
set tabstop=4
set shiftwidth=4

" Enable syntax highlighting
syntax on

" Highlight search results as you type
set incsearch

" Highlight current line
set cursorline

" Enable mouse in all modes
set mouse=a

" Enable file type detection
filetype on
