# Pickling is the conversion of python objects to binary form.
# Whereas, unpickling is the conversion of binary form data to python objects.
# The pickled objects are used for storing in disks or external memory locations. Unpickled objects are used for getting the data
# back as python objects upon which processing can be done in python.
# Python provides a pickle module for achieving this.
# Pickling uses the pickle.dump() method to dump python objects into disks.
# Unpickling uses the pickle.load() method to get back the data as python objects.
